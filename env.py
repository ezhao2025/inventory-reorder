# ── task templates ────────────────────────────────────────────────────────────
# One named @env.template per difficulty. HUD discovers tasks by NAME, and a task
# is produced by CALLING the template (in tasks.py) — mirroring the autobiz template
# where `_one_deal = one_deal(prompt=...)`. Each template binds its own season, then
# runs the shared verifier. The prompt is passed in from tasks.py.

async def _run_season(season: SeasonConfig, name: str, prompt: str) -> AsyncGenerator[Any, Any]:
    """Shared episode body: bind the season, let the agent act, then score."""
    global _SEASON, _POLICY
    _SEASON = season
    _POLICY = None

    yield prompt  # the agent works through the tools; its text reply isn't graded

    reward, info = _score()
    logger.info("inventory[%s] reward=%.3f (%s)", name, reward, info.get("reason", ""))
    yield EvaluationResult(reward=reward, content=info.get("reason", ""), info=info)


@env.template()
async def inventory_easy(prompt: str) -> AsyncGenerator[Any, Any]:
    """Easy: 1 SKU, stable demand, short lead time."""
    async for item in _run_season(_EASY_SEASON, "inventory_easy", prompt):
        yield item


@env.template()
async def inventory_medium(prompt: str) -> AsyncGenerator[Any, Any]:
    """Medium: 3 SKUs, mild seasonality, one demand spike."""
    async for item in _run_season(_MEDIUM_SEASON, "inventory_medium", prompt):
        yield item


@env.template()
async def inventory_hard(prompt: str) -> AsyncGenerator[Any, Any]:
    """Hard: 5 SKUs, long lead times, two spikes, tighter cost ratio."""
    async for item in _run_season(_HARD_SEASON, "inventory_hard", prompt):
        yield item


@env.template()
async def inventory_sequential(prompt: str) -> AsyncGenerator[Any, Any]:
    """Sequential: the agent reorders week-by-week, observing state and adapting
    to demand as the season unfolds (vs. the one-shot tasks' single static policy)."""
    global _SEQ, _SEASON, _SEQ_INTERVAL
    _SEASON = _MEDIUM_SEASON          # 3 SKUs with a mid-season spike — good for adapting
    _SEQ_INTERVAL = 7                 # weekly decisions (~9 over the 60-day season)
    _SEQ = SequentialSeason(_SEASON, interval=_SEQ_INTERVAL)
    _SEQ.reset()

    yield prompt  # the agent drives the season via get_state / place_order

    reward, info = _seq_score()
    logger.info("inventory_sequential reward=%.3f (%s)", reward, info.get("reason", ""))
    yield EvaluationResult(reward=reward, content=info.get("reason", ""), info=info)

