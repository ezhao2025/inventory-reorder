"""inventory-reorder: a demand-driven inventory environment (HUD v6).

One autonomous-business loop: an agent manages reordering for a small catalog of
SKUs across a simulated season. It reads each SKU's costs, lead time, and recent
demand, then submits a reorder policy (order-up-to-S target levels). The verifier
runs the full seeded season deterministically and scores the agent on a TRUE REWARD
FUNCTION — holding cost vs. stockout cost — normalized between a do-nothing floor
and an order-up-to-S reference. >1.0 means it beat the reference heuristic.

Mirrors the autobiz-kernel pattern: the agent submits once via a tool, and a
deterministic, offline, key-free verifier runs the hidden dynamics (stochastic but
seeded demand, planted spikes) to produce the score. No memorization shortcut: the
demand the agent is scored on is generated by the verifier, not shown in full.
"""

